function initSidebar() {
    const body = document.body;
    const sidebar = document.querySelector(".sidebar");
    const sidebarToggle = document.querySelector("#sidebarToggle");

    if (!sidebar || !sidebarToggle) return;

    // Buang event lama, clone biar fresh
    const freshToggle = sidebarToggle.cloneNode(true);
    sidebarToggle.parentNode.replaceChild(freshToggle, sidebarToggle);

    // Tambahkan event click baru
    freshToggle.addEventListener("click", function (e) {
        e.preventDefault();

        body.classList.toggle("sidebar-toggled");
        sidebar.classList.toggle("toggled");

        if (sidebar.classList.contains("toggled")) {
            sidebar.querySelectorAll(".collapse").forEach(elem => {
                try {
                    let collapse = bootstrap.Collapse.getInstance(elem);
                    if (collapse) collapse.dispose();

                    new bootstrap.Collapse(elem, { toggle: false }).hide();
                } catch (e) {
                    console.warn("Collapse reset gagal:", e);
                }
            });
        }
    });
}

// Inisialisasi pertama kali
document.addEventListener("DOMContentLoaded", initSidebar);

// Re-init setiap selesai navigasi Livewire
document.addEventListener("livewire:navigated", initSidebar);
</script>


